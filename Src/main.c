/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	SCB->VTOR = FLASH_BASE | 0x10000;
	__set_FAULTMASK(0);
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC3_Init();
  MX_I2C2_Init();
  MX_SPI2_Init();
  MX_USART1_UART_Init();
  MX_USART6_UART_Init();
  MX_USART3_UART_Init();
  MX_DAC_Init();
  //MX_LWIP_Init();
  
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
	PLATFORM_Init();
	MX_LWIP_Init();
  my_http_init();
	
	HAL_GPIO_WritePin(NET_RELAY_EN_GPIO_Port, NET_RELAY_EN_Pin, GPIO_PIN_SET);   //使能网络
	HAL_GPIO_WritePin(OUT_RELAY_EN_GPIO_Port, OUT_RELAY_EN_Pin, GPIO_PIN_SET);   //使能继电器输出
	
	//static RTC_TimeTypeDef stimestructureget;
	//static RTC_DateTypeDef sdatestructureget;
	
	static uint32_t Tick = 0;
	Tick = HAL_GetTick();
	static uint32_t Init_tick = 0;
	if(eMBTCPInit(502) != MB_ENOERR) 
		Soft_Reset();
	eMBEnable();
	
	MX_WWDG_Init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		eMBPoll();
		if(HAL_GetTick() - Tick >= 1000) {
			
			HAL_GPIO_TogglePin(APP_RUN_GPIO_Port, APP_RUN_Pin);
			Tick = HAL_GetTick();
			//检测内存
			struct tcp_seg *seg = (struct tcp_seg *)memp_malloc(MEMP_TCP_SEG);
			if(seg == NULL)
				Soft_Reset();
			memp_free(MEMP_TCP_SEG, seg);

		}
		if(HAL_GPIO_ReadPin(INIT_KEY_GPIO_Port, INIT_KEY_Pin) == GPIO_PIN_SET)
			Init_tick = 0;
		if(HAL_GPIO_ReadPin(INIT_KEY_GPIO_Port, INIT_KEY_Pin) == GPIO_PIN_RESET && !Init_tick) {
			Init_tick = HAL_GetTick();
		}
		if(Init_tick && HAL_GetTick() - Init_tick >= 3000) {
			Init_tick = HAL_GetTick();
			ClearSysInfo();
			Soft_Reset();
		}
		WWDG_Refresh();
		MX_LWIP_Process();
  }
  /* USER CODE END 3 */

}

